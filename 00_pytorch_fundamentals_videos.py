"""## Reproducbility (trying to take random out of random)

In short how a neural network learns:

'start with randon numbers -> tensor operations -> update random numbers to try and
make them better representations of the data -> again -> again -> again... '

To reduce the randomness in neural networks and PyTorch comes the concept of a **random seed**.

Essentially what the random seed does id "flavour" the randomness.
"""

import torch

#Create two random tensors
random_tensor_A = torch.rand(3,4)
random_tensor_B = torch.rand(3,4)

print(random_tensor_A)
print(random_tensor_B)
print(random_tensor_A==random_tensor_B)

# Let's make some random but reproducible tensors
import torch

#set the random seed
RANDOM_SEED = 100
torch.manual_seed(RANDOM_SEED)
random_tensor_C = torch.rand(3,4)

torch.manual_seed(100)
