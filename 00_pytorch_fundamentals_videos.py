"""### Matrix multiplication

Two main ways of performing multiplication in nueral networks and deep learning:

1. Element-wise multiplication
2. Matrix multiplication(dot product)

There are two main rules that performing matrix multiplicaion needs to satisfy:
1. The **inner dimensions** must match:
* '(3,2) @ (3,2)' won't work
* '(2,3) @ (3,2)' will work
* '(3,2) @ (2,3)' will work
2. The resulting matirix has the shape of the **outer dimentions**:
* '(2,3) @ (3,2)' -> '(2,2)'
* '(3,2) @ (2,3)' -> '(3,3)'
"""

# Element wise multiplication
